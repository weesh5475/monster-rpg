// ── field1 대형 맵 생성 (18단계) ──────────────────────────────
// 거대한 배열을 손으로 쓰지 않고 결정적(시드 고정)으로 생성한다.
// 같은 시드 → 항상 같은 맵 → 워프/아이템 좌표가 고정이라 저장과 호환.
export const FIELD_W = 48;
export const FIELD_H = 48;

// 결정적 의사난수(mulberry32)
function mulberry32(seed) {
  let a = seed >>> 0;
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

// 걷기 가능한 칸만 따라 START 에서 BFS → must 목록이 전부 닿는지 검증(개발용 경고)
function checkConnectivity(tiles, must) {
  const H = tiles.length;
  const W = tiles[0].length;
  const walkable = (c) => c === TILE.GROUND || c === TILE.GRASS || c === TILE.DOOR;
  const seen = Array.from({ length: H }, () => new Array(W).fill(false));
  const [sx, sy] = must[0];
  const stack = [[sx, sy]];
  seen[sy][sx] = true;
  while (stack.length) {
    const [x, y] = stack.pop();
    for (const [dx, dy] of [[1, 0], [-1, 0], [0, 1], [0, -1]]) {
      const nx = x + dx;
      const ny = y + dy;
      if (nx < 0 || ny < 0 || nx >= W || ny >= H || seen[ny][nx]) continue;
      if (!walkable(tiles[ny][nx])) continue;
      seen[ny][nx] = true;
      stack.push([nx, ny]);
    }
  }
  must.forEach(([x, y, label]) => {
    if (!seen[y][x]) console.warn(`[buildField] 도달 불가 칸: ${label || ''} (${x},${y})`);
  });
}

function buildField() {
  const W = FIELD_W;
  const H = FIELD_H;
  const rnd = mulberry32(20250618); // 고정 시드

  // 1) 전부 길로 채움
  const t = Array.from({ length: H }, () => new Array(W).fill(TILE.GROUND));
  // 2) 바깥 경계 = 벽(나무)
  for (let x = 0; x < W; x++) {
    t[0][x] = TILE.WALL;
    t[H - 1][x] = TILE.WALL;
  }
  for (let y = 0; y < H; y++) {
    t[y][0] = TILE.WALL;
    t[y][W - 1] = TILE.WALL;
  }
  // 사각형 채우기(경계 보호)
  const fillRect = (x0, y0, x1, y1, code) => {
    for (let y = Math.max(1, y0); y <= Math.min(H - 2, y1); y++)
      for (let x = Math.max(1, x0); x <= Math.min(W - 2, x1); x++) t[y][x] = code;
  };

  // 3) 호수(물) + 가운데 섬 문(route2). 주변엔 길 여백이 있어 돌아갈 수 있음.
  fillRect(30, 18, 38, 26, TILE.WATER);
  t[22][34] = TILE.DOOR; // 섬 문 → route2 (파도타기로 도달)

  // 4) 키큰풀 패치 여러 곳(걷기 가능 → 연결성에 영향 없음)
  [
    [5, 10, 10, 14], [14, 5, 19, 9], [22, 30, 28, 35],
    [36, 8, 42, 13], [8, 33, 14, 38], [30, 38, 36, 42],
  ].forEach(([a, b, c, d]) => fillRect(a, b, c, d, TILE.GRASS));
  // 시드로 약간의 흩뿌림(현재 길인 칸에만)
  for (let i = 0; i < 140; i++) {
    const x = 2 + Math.floor(rnd() * (W - 4));
    const y = 2 + Math.floor(rnd() * (H - 4));
    if (t[y][x] === TILE.GROUND) t[y][x] = TILE.GRASS;
  }

  // 5) 나무 군락(벽) — 작은 덩어리. 길이 둘러싸 연결을 끊지 않게.
  [
    [12, 18, 14, 20], [20, 14, 22, 16], [40, 28, 42, 30],
    [6, 24, 8, 26], [26, 6, 28, 8], [34, 34, 36, 36], [18, 40, 21, 42],
  ].forEach(([a, b, c, d]) => fillRect(a, b, c, d, TILE.WALL));

  // 6) 비밀 구역 — 외벽으로 막은 골방 + 게이트(나무/바위)
  // 자르기 골방(불꽃배지): 아이템(40,7) / 나무 게이트(40,8) / 접근(40,9)
  fillRect(39, 6, 41, 9, TILE.WALL);
  t[7][40] = TILE.GROUND;
  t[8][40] = TILE.CUT_TREE;
  t[9][40] = TILE.GROUND;
  // 괴력 골방(물배지): 아이템(7,40) / 바위(8,40) / 알코브(8,39) / 접근(8,41)
  fillRect(6, 38, 9, 41, TILE.WALL);
  t[40][7] = TILE.GROUND;
  t[40][8] = TILE.PUSH_ROCK;
  t[39][8] = TILE.GROUND;
  t[41][8] = TILE.GROUND;

  // 7) 주요 지점은 반드시 길로 보장(워프/도착/시작/트레이너/NPC/아이템)
  const g = (x, y) => {
    t[y][x] = TILE.GROUND;
  };
  t[H - 1][24] = TILE.DOOR; // 아래 경계 문 → town1
  g(24, H - 2); // 문 앞
  g(29, 22); // route2 서쪽 물가 도착 칸
  g(3, 3); // 시작/복귀
  [[6, 20], [20, 8], [33, 38], [12, 30]].forEach(([x, y]) => g(x, y)); // 아이템볼
  [[15, 15], [30, 30], [5, 7]].forEach(([x, y]) => g(x, y)); // 트레이너/NPC
  g(40, 9); // 비밀 접근 칸
  g(8, 41);

  // 8) 연결성 검증(개발 경고): 걸어서 닿아야 하는 칸들
  checkConnectivity(t, [
    [3, 3, 'START'], [24, H - 2, 'town1문 앞'], [29, 22, 'route2 물가'],
    [6, 20, 'ib1'], [20, 8, 'ib2'], [33, 38, 'ib3'], [12, 30, 'ib4'],
    [15, 15, '트레이너1'], [30, 30, '트레이너2'], [5, 7, 'NPC'],
    [24, H - 1, 'town1문'], [40, 9, '자르기 접근'], [8, 41, '괴력 접근'],
  ]);

  return t;
}

