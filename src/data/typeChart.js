// 공격타입 1개 vs 방어 몬스터의 타입 배열 → 최종 배율(0, 0.25, 0.5, 1, 2, 4 ...)
export function getTypeMultiplier(attackType, defenderTypes) {
  const row = TYPE_CHART[attackType] || {};
  let multiplier = 1;
  for (const defType of defenderTypes) {
    if (Object.prototype.hasOwnProperty.call(row, defType)) {
      multiplier *= row[defType];
    }
  }
  return multiplier;
}
